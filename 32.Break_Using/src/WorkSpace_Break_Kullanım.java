
public class WorkSpace_Break_Kullanım {
	public static void main(String[] args) {

		// Break Kullanımı
		// Break'i döngüde istediğim bir değer gelince orada döngüyü kırması ve döngüden çıkılması için kullanıyorum
		// Bunu genellikle istediğim bir değeri yakalamak için kullanıyorum
		
		int yakala4 ;
		
		for (int i = 1; i <= 10 ; i++) {
			if (i==4) {
				yakala4=i;
				System.out.println(yakala4);
				break;// döngüyü kır - döngüden çık
			}
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
